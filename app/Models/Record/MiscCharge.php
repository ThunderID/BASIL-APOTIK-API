<?php

namespace App\Models\Record;

use Illuminate\Database\Eloquent\Model;

class MiscCharge extends Model
{
    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// TRAITS
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// VARIABLES
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	protected $fillable = [
		'org_id',
		'reception_id',
		'room_id',
		'no',
		'guest_name',
		'type',
		'lines',
		'created_by',
		'approved_by',
		'approved_at',
	];

	public $timestamps = true;

	protected $hidden = [
	];

	protected $casts = [
		'lines'		=> 'array'
	];

	protected $dates = [
		'deleted_at', 'approved_at'
	];

	protected $touches = [
	];

	protected $observables = [
	];

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// CONFIGURATIONS
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	CONST TYPE 		= ['MISC_CHARGE', 'LOST_AND_BREAKAGE'];


	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// BOOT
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// CONSTRUCT
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// RELATIONSHIP
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public function org() {
		return $this->belongsTo(\App\Org::class);
	}

	public function reception() {
		return $this->belongsTo(Reception::class);
	}

	public function room() {
		return $this->belongsTo(\App\Room::class);
	}

	public function creator() {
		return $this->belongsTo(\App\User::class, 'created_by');
	}

	public function approver() {
		return $this->belongsTo(\App\User::class, 'approved_by');
	}
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// BOOT
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// STATIC FUNCTION
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// FUNCTION
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public function getRules()
	{
		$rules['org_id']		= ['required', 'integer', 'exists:'. app()->make(\App\Org::class)->getTable() . ',id'];
		$rules['room_id']		= ['required', 'integer', 'exists:'. app()->make(\App\Room::class)->getTable() . ',id'];
		$rules['reception_id']	= ['required', 'integer', 'exists:'. app()->make(Reception::class)->getTable() . ',id'];
		$rules['no'] 			= ['nullable', 'string'];
		$rules['guest_name']	= ['required', 'string'];
		$rules['type']   		= ['required', 'string', 'in:'.implode(',', SELF::TYPE)];
		$rules['lines']			= ['required', 'array'];
		$rules['lines.*.description']	= ['required', 'string'];
		$rules['lines.*.amount']  		= ['required', 'numeric'];
		$rules['created_by'] 	= ['required', 'integer', 'exists:'. app()->make(\App\User::class)->getTable() . ',id'];
		$rules['approved_by'] 	= ['nullable', 'integer', 'exists:'. app()->make(\App\User::class)->getTable() . ',id'];
		$rules['approved_at']	= ['required_with:approved_by', 'date'];
		
		return $rules;
	}
	
	public function generateNo() {
		$prefix 	= str_pad($this->org_id, 4, '0', STR_PAD_LEFT).'.'.now()->format('ym');
		$idx 		= self::where('no', 'like', $prefix.'%')->count() + 1;
		do{
			$no 	= $prefix.'.'.str_pad($idx, 4, '0', STR_PAD_LEFT);
			$exists	= self::where('no', $no)->first();
			$idx++;
		}while ($exists);
		
		return $no;
	}

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// ACCESSOR
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public function getAmountAttribute()
	{
		return array_sum(array_column($this->lines, 'amount'));
	}

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// MUTATOR
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// QUERY
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public function scopeGuestName ($q, String $v) 
	{
		return $q->where('guest_name', 'like', str_replace('*', '%', $v));
	}
}
