<?php

namespace App\Models\Membership;

use Illuminate\Database\Eloquent\Model;

class MemberPoint extends Model
{
    // --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// TRAITS
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// VARIABLES
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	protected $fillable = [
		'user_id',
		'partner_id',
		'date',
		'amount',
		'net_revenue',
		'ref_id',
		'ref_type',
		'note', 
		'org_group_id'
	];

	public $timestamps = true;

	protected $hidden = [
	];

	protected $casts = [
	];

	protected $dates = [
		'deleted_at',
		'date'
	];

	protected $touches = [
	];

	protected $observables = [
	];

	protected $appends = [
		'balance'
	];
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// CONFIGURATIONS
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// BOOT
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// CONSTRUCT
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// RELATIONSHIP
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public function partner() {
		return $this->belongsTo(\App\Partner::class);
	}

	public function user() {
		return $this->belongsTo(\App\User::class);
	}

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// BOOT
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// STATIC FUNCTION
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// FUNCTION
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public function getRules()
	{
		$rules['org_group_id']	= ['required', 'integer', 'exists:'. app()->make(\App\OrgGroup::class)->getTable() . ',id'];
		$rules['user_id']		= ['nullable', 'integer', 'exists:'. app()->make(\App\User::class)->getTable() . ',id'];
		$rules['partner_id']	= ['nullable', 'integer', 'exists:'. app()->make(\App\Partner::class)->getTable() . ',id'];
		$rules['date']			= ['required', 'date'];
		$rules['amount']		= ['required', 'numeric'];
		$rules['ref_id']		= ['required', 'integer'];
		$rules['ref_type']		= ['required', 'string'];
		
		return $rules;
	}
	
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// ACCESSOR
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// MUTATOR
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public function getBalanceAttribute(){
		return MemberPoint::where('user_id', $this->user_id)->where('partner_id', $this->partner_id)->where('org_group_id', $this->org_group_id)->where('date', '<=', $this->date)->sum('amount');	
	}

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// QUERY
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
}
